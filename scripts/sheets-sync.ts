function getLongestCellLineLength(value: string): number {
    return value
        .split(/\r?\n/gu)
        .reduce((longest, line) => Math.max(longest, line.length), 0);
}

function estimateColumnPixelWidth(rows: string[][], columnIndex: number): number {
    const longestCellLength = rows.reduce((longest, row) => {
        return Math.max(longest, getLongestCellLineLength(row[columnIndex] ?? ''));
    }, 0);

    return Math.max(36, longestCellLength * 7 + 16);
}

function buildColumnWidthRequests(sheetId: number, rows: string[][]): sheets_v4.Schema$Request[] {
    return Array.from({ length: SHEET_HEADERS.length }, (_, columnIndex) => ({
        updateDimensionProperties: {
            range: {
                sheetId,
                dimension: 'COLUMNS',
                startIndex: columnIndex,
                endIndex: columnIndex + 1,
            },
            properties: {
                pixelSize: estimateColumnPixelWidth(rows, columnIndex),
            },
            fields: 'pixelSize',
        },
    }));
}

function normalizeDriveResourceId(value: string | undefined): string | undefined {
    if (!value) {
        return undefined;
    }

    const trimmed = value.trim();
    if (trimmed === '') {
        return undefined;
    }

    const folderMatch = trimmed.match(/\/folders\/([a-zA-Z0-9_-]+)/u);
    if (folderMatch) {
        return folderMatch[1];
    }

    const idMatch = trimmed.match(/[?&]id=([a-zA-Z0-9_-]+)/u);
    if (idMatch) {
        return idMatch[1];
    }

    return trimmed;
}

function formatGoogleApiError(error: unknown): string {
    if (!error || typeof error !== 'object') {
        return String(error);
    }

    const candidate = error as { message?: unknown; cause?: unknown };
    const directMessage = typeof candidate.message === 'string' ? candidate.message : undefined;

    if (candidate.cause && typeof candidate.cause === 'object') {
        const cause = candidate.cause as { message?: unknown; status?: unknown; code?: unknown };
        const causeMessage = typeof cause.message === 'string' ? cause.message : undefined;
        const causeStatus = typeof cause.status === 'string' ? cause.status : typeof cause.code === 'number' ? String(cause.code) : undefined;

        if (causeMessage && causeStatus) {
            return `${causeMessage} (${causeStatus})`;
        }

        if (causeMessage) {
            return causeMessage;
        }
    }

    return directMessage ?? String(error);
}

async function fileExists(filePath: string): Promise<boolean> {
    try {
        await stat(filePath);
        return true;
    } catch {
        return false;
    }
}

async function resolveSeriesFile(regionPath: string, generationKey: string, seriesKey: string): Promise<string> {
    const yamlPath = path.join(regionPath, `${generationKey}:${seriesKey}.yaml`);
    if (await fileExists(yamlPath)) {
        return yamlPath;
    }

    const ymlPath = path.join(regionPath, `${generationKey}:${seriesKey}.yml`);
    if (await fileExists(ymlPath)) {
        return ymlPath;
    }

    throw new Error(`Series file not found for ${generationKey}:${seriesKey} under ${toRelativePath(regionPath)}.`);
}

